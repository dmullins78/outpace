package org.dmullins.outpace.number;

public class ASCIIThree extends ASCIINumber {

    public int getNumber() {
        return 3;
    }

    String getFirst() {
        return " _ ";
    }

    String getSecond() {
        return " _|";
    }

    String getThird() {
        return " _|";
    }

}
