package org.dmullins.outpace.number;

public class ASCIIFour extends ASCIINumber {

    public int getNumber() {
        return 4;
    }

    String getFirst() {
        return "   ";
    }

    String getSecond() {
        return "|_|";
    }

    String getThird() {
        return "  |";
    }

}
