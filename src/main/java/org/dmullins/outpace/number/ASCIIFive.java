package org.dmullins.outpace.number;

public class ASCIIFive extends ASCIINumber {

    public int getNumber() {
        return 5;
    }

    String getFirst() {
        return " _ ";
    }

    String getSecond() {
        return "|_ ";
    }

    String getThird() {
        return " _|";
    }

}
