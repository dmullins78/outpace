package org.dmullins.outpace.number;

public class ASCIIEight extends ASCIINumber {

    public int getNumber() {
        return 8;
    }

    String getFirst() {
        return " _ ";
    }

    String getSecond() {
        return "|_|";
    }

    String getThird() {
        return "|_|";
    }

}
